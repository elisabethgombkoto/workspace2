package hue03.Chees;

public class Hole {
	private int holeNummber;
	private int holeSize;

	public Hole(int holeNummber, int holeSize) {
		this.holeNummber = holeNummber;
		this.holeSize = holeSize;
	}

	public int getHoleNummber() {
		return holeNummber;
	}

	public void setHoleNummber(int holeNummber) {
		this.holeNummber = holeNummber;
	}

	public int getHoleSize() {
		return holeSize;
	}

	public void setHoleSize(int holeSize) {
		this.holeSize = holeSize;
	}

}
